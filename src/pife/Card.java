package pife;

public class Card{
    private int number = 0;
    private int suit = 0;

    public int getNumber() {
        return number;
    }
    public int getSuit() {
        return suit;
    }

    public void setNumber(int number) {
        this.number = number;
    }
    public void setSuit(int suit) {
        this.suit = suit;
    }
}
